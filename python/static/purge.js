// Purge 视图 - 项目构建产物清理
(function () {
"use strict";
const M = window.MOLE;

M.addStrings({
    zh: { p_title: "清理项目构建产物 (node_modules / target / dist …)",
          p_scan: "扫描项目", p_clean: "清理选中产物", p_selected: "已选",
          p_recent: "近期", p_roots: "扫描根目录",
          p_confirm: (n, s) => `确定永久删除 ${n} 个构建产物（约 ${s}）吗？这些目录可由 install/build 重新生成。`,
          p_done: (s, n) => `已释放 <b>${s}</b>，删除 ${n} 个产物目录`,
          p_preview: s => `预览：选中产物约 <b>${s}</b>（未删除）`, p_empty: "未发现可清理的项目产物" },
    en: { p_title: "Purge project build artifacts (node_modules / target / dist …)",
          p_scan: "Scan projects", p_clean: "Purge selected", p_selected: "Selected",
          p_recent: "recent", p_roots: "Scan roots",
          p_confirm: (n, s) => `Delete ${n} build artifacts (~${s})? They can be regenerated by install/build.`,
          p_done: (s, n) => `Freed <b>${s}</b>, removed ${n} artifact dirs`,
          p_preview: s => `Preview: ~<b>${s}</b> selected (nothing deleted)`, p_empty: "No project artifacts found" },
    ja: { p_title: "プロジェクト成果物を削除 (node_modules / target / dist …)",
          p_scan: "プロジェクト走査", p_clean: "選択を削除", p_selected: "選択中",
          p_recent: "最近", p_roots: "走査ルート",
          p_confirm: (n, s) => `${n} 個の成果物（約 ${s}）を削除しますか？install/build で再生成できます。`,
          p_done: (s, n) => `<b>${s}</b> 解放、${n} 個削除`,
          p_preview: s => `プレビュー：約 <b>${s}</b>（削除なし）`, p_empty: "成果物が見つかりません" },
});

let projects = [];   // {name,path,size,recent,artifacts:[{name,path,size}]}
let scanned = false;

function render(el) {
    el.innerHTML = `
      <h2 class="view-title">${M.t("p_title")}</h2>
      <div class="actionbar">
        <button class="btn btn-primary" id="pg-scan">${M.t("p_scan")}</button>
        <button class="btn btn-danger" id="pg-clean" disabled>${M.t("p_clean")}</button>
        <label class="chk preview-toggle"><input type="checkbox" id="pg-dry" checked><span>${M.t("preview")}</span></label>
        <div class="spacer"></div>
        <div class="totals"><span class="totals-label">${M.t("p_selected")}</span><span class="totals-val" id="pg-tot">—</span></div>
      </div>
      <div class="roots" id="pg-roots"></div>
      <div class="progress" id="pg-prog" style="display:none"><div class="progress-fill" id="pg-progfill"></div><span class="progress-text" id="pg-progtxt"></span></div>
      <div class="proj-list" id="pg-list"></div>
      <div class="result-banner" id="pg-banner" style="display:none"></div>`;
    el.querySelector("#pg-scan").addEventListener("click", startScan);
    el.querySelector("#pg-clean").addEventListener("click", startClean);
    if (scanned) renderList();
}

function renderList() {
    const list = M.$("pg-list"); if (!list) return;
    if (!projects.length) { list.innerHTML = `<p class="placeholder">${M.t("p_empty")}</p>`; return; }
    list.innerHTML = "";
    projects.sort((a, b) => b.size - a.size).forEach((p, pi) => {
        const card = document.createElement("div");
        card.className = "proj-card";
        let arts = "";
        p.artifacts.sort((a, b) => b.size - a.size).forEach((a, ai) => {
            const checked = p.recent ? "" : "checked";
            arts += `<label class="art" data-path="${M.escapeHtml(a.path)}">
                       <input type="checkbox" class="art-chk" ${checked}>
                       <span class="art-name">${M.escapeHtml(a.name)}</span>
                       <span class="art-size">${M.fmtSize(a.size)}</span></label>`;
        });
        card.innerHTML =
            `<div class="proj-head">
               <span class="proj-name">${M.escapeHtml(p.name)}</span>
               ${p.recent ? `<span class="tag-recent">${M.t("p_recent")}</span>` : ""}
               <span class="proj-path">${M.escapeHtml(p.path)}</span>
               <span class="spacer"></span>
               <span class="proj-size">${M.fmtSize(p.size)}</span>
             </div>
             <div class="arts">${arts}</div>`;
        list.appendChild(card);
    });
    list.querySelectorAll(".art-chk").forEach(c => c.addEventListener("change", updateTotal));
    updateTotal();
}

function selected() {
    return [...document.querySelectorAll("#pg-list .art")]
        .filter(a => a.querySelector(".art-chk").checked)
        .map(a => ({ path: a.dataset.path, size: sizeOf(a.dataset.path) }));
}
function sizeOf(path) {
    for (const p of projects) for (const a of p.artifacts) if (a.path === path) return a.size;
    return 0;
}
function updateTotal() {
    const sel = selected(); let t = 0; sel.forEach(s => t += s.size);
    const e = M.$("pg-tot"); if (e) e.textContent = M.fmtSize(t);
    const b = M.$("pg-clean"); if (b) b.disabled = !sel.length;
}

function prog(p, t) { M.$("pg-prog").style.display = ""; M.$("pg-progfill").style.width = p + "%"; M.$("pg-progtxt").textContent = t; }
function progHide() { const e = M.$("pg-prog"); if (e) e.style.display = "none"; }

function startScan() {
    projects = []; scanned = false;
    const btn = M.$("pg-scan"); btn.disabled = true; btn.textContent = M.t("scanning");
    M.$("pg-banner").style.display = "none";
    M.$("pg-list").innerHTML = `<p class="placeholder">${M.t("scanning")}</p>`;
    prog(50, M.t("scanning"));
    M.sse("/api/purge/scan", {
        onMsg: m => {
            if (m.type === "roots") { M.$("pg-roots").textContent = M.t("p_roots") + ": " + m.roots.join("  ·  "); }
            else if (m.type === "project") { projects.push(m); renderList(); prog(60, `${M.t("scanning")} ${projects.length}`); }
        },
        onDone: () => { scanned = true; progHide(); btn.disabled = false; btn.textContent = M.t("p_scan"); renderList(); },
        onError: () => { progHide(); btn.disabled = false; btn.textContent = M.t("p_scan"); },
    });
}

function startClean() {
    const sel = selected(); if (!sel.length) return;
    const dry = M.$("pg-dry").checked;
    let t = 0; sel.forEach(s => t += s.size);
    if (!dry && !confirm(M.t("p_confirm")(sel.length, M.fmtSize(t)))) return;
    const btn = M.$("pg-clean"); btn.disabled = true; M.$("pg-scan").disabled = true;
    const paths = sel.map(s => s.path);
    prog(0, M.t("p_clean")); let done = 0;
    // POST + 流式读取
    fetch("/api/purge/clean", {
        method: "POST", headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ paths, dry }),
    }).then(resp => {
        const reader = resp.body.getReader(); const dec = new TextDecoder(); let buf = "";
        function pump() {
            return reader.read().then(({ done: rd, value }) => {
                if (rd) return;
                buf += dec.decode(value, { stream: true });
                let idx;
                while ((idx = buf.indexOf("\n\n")) >= 0) {
                    const line = buf.slice(0, idx).trim(); buf = buf.slice(idx + 2);
                    if (!line.startsWith("data: ")) continue;
                    const m = JSON.parse(line.slice(6));
                    if (m.type === "item") {
                        done++; prog(done / paths.length * 100, `${M.t("p_clean")} ${done}/${paths.length}`);
                        if (!m.dry && m.ok) {
                            const a = document.querySelector(`#pg-list .art[data-path="${CSS.escape(m.path)}"]`);
                            if (a) { a.classList.add("cleaned"); a.querySelector(".art-size").textContent = "✓"; }
                        }
                    } else if (m.type === "done") {
                        progHide(); btn.textContent = M.t("p_clean"); M.$("pg-scan").disabled = false;
                        const b = M.$("pg-banner");
                        if (m.dry) { b.className = "result-banner preview"; b.innerHTML = M.t("p_preview")(M.fmtSize(m.total_freed)); }
                        else {
                            b.className = "result-banner ok"; b.innerHTML = M.t("p_done")(M.fmtSize(m.total_freed), m.removed);
                            // 移除已删产物
                            if (!m.dry) projects.forEach(p => p.artifacts = p.artifacts.filter(ar => {
                                const el = document.querySelector(`#pg-list .art[data-path="${CSS.escape(ar.path)}"]`);
                                return !(el && el.classList.contains("cleaned"));
                            }));
                        }
                        b.style.display = ""; updateTotal();
                    }
                }
                return pump();
            });
        }
        return pump();
    }).catch(() => { progHide(); btn.textContent = M.t("p_clean"); M.$("pg-scan").disabled = false; });
}

M.registerView("purge", { render });
})();
